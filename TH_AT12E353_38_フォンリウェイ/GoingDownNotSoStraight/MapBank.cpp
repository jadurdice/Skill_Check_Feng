MapBluePrint GetGoalRoom(int location) {

	MapBluePrint temp = RoomGoal;

	int way;
	do
	{

		way = GameSystem::RandInt(0, 99);

		if (way < 10) {
			way = 0;
		}
		else if (way >= 10 && way < 30) {
			way = 1;
		}
		else if (way >= 30 && way < 70) {
			way = 2;
		}
		else {
			way = 3;
		}

		switch (way)
		{
		case 0:break;
		case 1:
			way = GameSystem::RandInt(0, 3);

			temp.roomWay = temp.roomWay ^ (1 << way);
			temp.roomTag = R_GoalRLD + way;

			break;

		case 2:
			way = GameSystem::RandInt(0, 5);

			switch (way)
			{
			case 0:
				temp.roomWay = WAYUP | WAYRIGHT;
				temp.roomTag = R_GoalUR;
				break;
			case 1:
				temp.roomWay = WAYDOWN | WAYRIGHT;
				temp.roomTag = R_GoalRD;
				break;

			case 2:
				temp.roomWay = WAYDOWN | WAYLEFT;
				temp.roomTag = R_GoalLD;
				break;

			case 3:
				temp.roomWay = WAYUP | WAYLEFT;
				temp.roomTag = R_GoalUL;
				break;

			case 4:
				temp.roomWay = WAYUP | WAYDOWN;
				temp.roomTag = R_GoalUD;
				break;

			case 5:
				temp.roomWay = WAYRIGHT | WAYLEFT;
				temp.roomTag = R_GoalRL;
				break;

			}

		case 3:

			way = GameSystem::RandInt(0, 3);

			temp.roomWay = (1 << way);
			temp.roomTag = R_GoalU + way;

			break;
		}

		if (location % 3 == 0 && (temp.roomWay & WAYLEFT)) {
			temp.roomWay = temp.roomWay ^ WAYLEFT;
		}
		if (location % 3 == 2 && (temp.roomWay & WAYRIGHT)) {
			temp.roomWay = temp.roomWay ^ WAYRIGHT;
		}
		if (location / 3 == 0 && (temp.roomWay & WAYUP)) {
			temp.roomWay = temp.roomWay ^ WAYUP;
		}
		if (location / 3 == 2 && (temp.roomWay & WAYDOWN)) {
			temp.roomWay = temp.roomWay ^ WAYDOWN;
		}

		way = 4;

		for (int i = 0; i < 4; i++)
		{
			if (!(temp.roomWay & (1 << i))) {
				way -= 1;
			}
		}

	} while (way == 0);



	return temp;

}

