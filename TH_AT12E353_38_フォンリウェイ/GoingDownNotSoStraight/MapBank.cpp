MapBluePrint GetRoom(int index)
{
	if (isNowTutor()) {
		return tutorMapPutOn;
	}

	int aroundVoid = index + 1;
	switch (aroundVoid) {
	case	R_URLD:

		return Get4WayRoom();

	case	R_URL:
	case	R_URD:
	case	R_RLD:
	case	R_ULD:

		return Get3WayRoom(aroundVoid - R_URL);

	case	R_UR:
	case	R_RD:
	case	R_LD:
	case	R_UL:
	case	R_UD:
	case	R_RL:

		return Get2WayRoom(aroundVoid - R_UR);

	}

	return Get4WayRoom();
}
